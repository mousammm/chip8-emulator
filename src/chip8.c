bool chip8_load_rom(chip8_t *chip8,const char *filename){
   FILE *file = fopen(filename, "rb");
   // if !file

   fseek(file,0,SEEK_END);
   long size = ftell(file);
   rewind(file);

   if(size>4096 - 0x200){
      fprintf(stderr, "ROM too big: %ld bytes\n",size);
      fclose(file);
      return false;
   }
   
   fread(&chip8->memory[0x200],1,size,file);
   fclose(file);
   return true; 
}

